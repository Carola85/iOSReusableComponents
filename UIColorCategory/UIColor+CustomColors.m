#import "UIColor+CustomColors.h"

@implementation UIColor (CustomColors)

+ (UIColor *) borderColor
{
    return [UIColor colorWithRed:0.271 green:0.655 blue:0.804 alpha:1.000];
}

+ (UIColor *) lightBorderColor
{
    return [UIColor colorWithRed:0.596 green:0.851 blue:0.953 alpha:1.000];
}

+ (UIColor *) backgroundColor
{
    return [UIColor colorWithRed:0.467 green:0.804 blue:0.933 alpha:1.000];
}

@end
